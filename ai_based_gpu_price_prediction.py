import pandas as pd
import numpy as np
import re
from sklearn.preprocessing import LabelEncoder
from sklearn.ensemble import RandomForestRegressor

df_old = pd.read_csv("GPU_benchmarks_v7.csv").reset_index(drop=True)
df_new = pd.read_csv("cleaned_gpu.csv").reset_index(drop=True)

df_old.columns = df_old.columns.str.strip()
df_new.columns = df_new.columns.str.strip()

df_old.rename(columns={"gpuName": "GPU_Model",
                       "price": "Price"}, inplace=True)

df_new.rename(columns={"Product": "GPU_Model"}, inplace=True)

def clean_price(x):
    if pd.isna(x):
        return np.nan
    x = re.sub(r"[^\d.]", "", str(x))
    return float(x) if x != "" else np.nan

df_old["Price"] = df_old["Price"].apply(clean_price)
df_new["Price"] = df_new["Price"].apply(clean_price)

